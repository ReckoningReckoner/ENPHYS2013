function [d1, d2, d3] = pder(f, x1, h1, x2, h2, x3, h3)
    switch nargin
    case 3
        d1 = der(f, x1, h1);
    case 5
        d1 = 1/3 .* (4 .* (f(x1 + h1, x2) - f(x1 - h1, x2))./(2*h1) - ((f(x1 + 2*h1, x2) - f(x1 - 2*h1, x2))./(4*h1)));
        d2 = 1/3 .* (4 .* (f(x1, x2 + h2) - f(x1, x2  - h2))./(2*h2) - ((f(x1, x2 + 2*h2) - f(x1, x2 - 2*h2))./(4*h2)));
    case 7
        d1 = 1/3 .* (4 .* (f(x1 + h1, x2, x3) - f(x1 - h1, x2, x3))./(2*h1) - ((f(x1 + 2*h1, x2, x3) - f(x1 - 2*h1, x2, x3))./(4*h1)));
        d2 = 1/3 .* (4 .* (f(x1, x2 + h2, x3) - f(x1, x2 - h2, x3))./(2*h2) - ((f(x1, x2 + 2*h2, x3) - f(x1, x2 - 2*h2, x3))./(4*h2)));
        d3 = 1/3 .* (4 .* (f(x1, x2, x3 + h3) - f(x1, x2, x3 - h3))./(2*h3) - ((f(x1, x2, x3 + 2*h3) - f(x1, x2, x3 - 2*h3))./(4*h3)));
    end
end
